def create_font_pcx():
    """Create a PCX image with the font character grid."""
    num_rows = (NUM_CHARS + CHARS_PER_ROW - 1) // CHARS_PER_ROW
    img_width = CHARS_PER_ROW * CHAR_WIDTH
    img_height = num_rows * CHAR_HEIGHT

    # Create palette: 256 colors
    # Color 0 = transparent (black)
    # Color 1 = white (foreground)
    # Color 2 = black (background, if needed)
    palette = [0, 0, 0] * 256  # All black by default
    palette[0*3:0*3+3] = [0, 0, 0]      # Index 0 = black/transparent
    palette[1*3:1*3+3] = [255, 255, 255]  # Index 1 = white
    palette[2*3:2*3+3] = [0, 0, 0]        # Index 2 = black

    # Create indexed image
    img = Image.new('P', (img_width, img_height), 0)
    img.putpalette(palette)

    # Draw characters
    pixels = img.load()
    for char_idx in range(NUM_CHARS):
        char_code = FIRST_CHAR + char_idx
        char = chr(char_code)
        if char not in FONT_DATA:
            continue

        glyph = FONT_DATA[char]
        row = char_idx // CHARS_PER_ROW
        col = char_idx % CHARS_PER_ROW
        x_offset = col * CHAR_WIDTH
        y_offset = row * CHAR_HEIGHT

        for y in range(CHAR_HEIGHT):
            row_bits = glyph[y] if y < len(glyph) else 0
            for x in range(CHAR_WIDTH):
                if row_bits & (0x80 >> x):
                    pixels[x_offset + x, y_offset + y] = 1  # white

    # Save as PCX
    buf = io.BytesIO()
    img.save(buf, format='PCX')
    return buf.getvalue()


def build_fnt():
    """Build a MUGEN v1 .fnt file."""
    # Generate PCX texture
    pcx_data = create_font_pcx()

    # Build DEF text section
    text_lines = [
        '[Def]',
        'type = fixed',
        f'size = {CHAR_WIDTH}, {CHAR_HEIGHT}, 0',
        'spacing = 0, 0, 0',
        'offset = 0, 0, 0',
        '',
        '[Map]',
    ]

    # Map each printable ASCII character to its index.
    # Use 0x hex notation for characters that can't be represented as
    # single-character keys in DEF format (space, etc.)
    for char_idx in range(NUM_CHARS):
        char_code = FIRST_CHAR + char_idx
        char = chr(char_code)
        # Use hex notation for space and other tricky chars
        if char_code <= 32 or char in ('',):
            key = f'0x{char_code:02x}'
        else:
            key = char
        text_lines.append(f'{key} {char_idx}')

    text_data = '\n'.join(text_lines) + '\n'
    text_bytes = text_data.encode('utf-8')

    # Calculate offsets
    header_size = 72
    texture_offset = header_size
    texture_length = len(pcx_data)
    text_offset = texture_offset + texture_length

    # Build header (72 bytes)
    header = bytearray(header_size)
    # Magic signature (12 bytes) — not validated by engine
    header[0:12] = b'ElecbyteFNT\x00'
    # Version: 0,1 (v1)
    struct.pack_into('<HH', header, 12, 0, 1)
    # Texture offset and length
    struct.pack_into('<II', header, 16, texture_offset, texture_length)
    # Text offset and length (length unused — engine computes from file end)
    struct.pack_into('<II', header, 24, text_offset, len(text_bytes))
    # Comment
    comment = b'FGE bitmap font v1 - ASCII 32-126'
    header[32:32+len(comment)] = comment

    # Assemble file
    fnt_data = bytes(header) + pcx_data + text_bytes

    # Verify text offset — find [Def] marker and adjust if needed
    marker_pos = fnt_data.find(b'[Def]')
    if marker_pos != text_offset:
        # Fix the text offset to point to the actual [Def] position
        struct.pack_into('<I', header, 24, marker_pos)
        fnt_data = bytes(header) + pcx_data + text_bytes
        print(f'  Fixed text offset: {text_offset} -> {marker_pos}')

    return fnt_data


def main():
    output_path = sys.argv[1] if len(sys.argv) > 1 else 'font/f-4x6.fnt'
    fnt_data = build_fnt()
    with open(output_path, 'wb') as f:
        f.write(fnt_data)
    print(f'Generated {output_path} ({len(fnt_data)} bytes)')
    print(f'  Characters: {NUM_CHARS} (ASCII {FIRST_CHAR}-{LAST_CHAR})')
    print(f'  Grid: {CHARS_PER_ROW}x{(NUM_CHARS+CHARS_PER_ROW-1)//CHARS_PER_ROW} chars')
    print(f'  Glyph size: {CHAR_WIDTH}x{CHAR_HEIGHT}px')


if __name__ == '__main__':
    main()
